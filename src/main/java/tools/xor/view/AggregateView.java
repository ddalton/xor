	private QueryView getQueryView(ViewKey viewKey) {
		if(viewCache.containsKey(viewKey))
			return viewCache.get(viewKey);
		
		QueryView result = new QueryView(viewKey, this);
		viewCache.put(viewKey, result);

		return result;
	}

	@Override
	public QueryView getEntityView(Type type, boolean narrow) {
		if(children != null) {
			for(View child: children)
				if( ((AggregateView)child).getChildren() != null && ((AggregateView)child).getChildren().size() > 0)
					throw new IllegalArgumentException("A content view cannot have more than one level of child content views");
		}
		
		QueryView result = getQueryView(new ViewKey(type, name, narrow));

		return result;
	}

	public Set<String> getParameterNames() {
		Set<String> result = new HashSet<>();

		if(parameter != null)
			for(Parameter param: parameter)
				result.add(param.name);

		return result;
	}

	public static boolean isBuiltInView(String viewName) {
		for(ViewType viewType: ViewType.values()) {
			if(viewName.endsWith(viewType.toString())) {
				return true;
			}
		}

		return false;
	}

	@Override
	public Class inferDomainClass() {
		String suffix = null;
		Class result = null;

		for(ViewType viewType: ViewType.values()) {
			if(name.endsWith(viewType.toString())) {
				suffix = Settings.URI_PATH_DELIMITER + viewType.toString();
				break;
			}
		}

		if(suffix != null) {
			String encodedName = name.substring(0, name.indexOf(suffix));
			String className = Settings.decodeParam(encodedName);
			try {
				result = Class.forName(className);
			}
			catch (ClassNotFoundException e) {
				result = null;
			}
		}

		return result;
	}

	@Override
	public AggregateView copy() {
		AggregateView result = new AggregateView();

		result.setName(name);
		if(attributeList != null) {
			result.setAttributeList(new ArrayList<>(attributeList));
		}

		if(filter != null) {
			List<Filter> filterCopy = new ArrayList<>();
			for(Filter f: filter) {
				filterCopy.add(new Filter(f.expression, f.position));
			}
			result.setFilter(filterCopy);
		}

		if(nativeQuery != null) {
			result.setNativeQuery(nativeQuery.copy());
		}

		if(children != null) {
			List<AggregateView> childrenCopy = new ArrayList<>();
			for(AggregateView c: children) {
				childrenCopy.add(c.copy());
			}
			result.setChildren(childrenCopy);
		}

		// TODO: can this be made immutable
		if(parameter != null) {
			result.setParameter(new ArrayList<>(parameter));
		}

		result.setJoin(join);
		result.setUnion(union);

		if(regexAttributes != null) {
			result.regexAttributes = new HashMap(regexAttributes);
		}
		if(exactAttributes != null) {
			result.exactAttributes = new HashSet(exactAttributes);
		}
		
		if(stateGraph != null) {
			for(Map.Entry<String, StateGraph<State, Edge<State>>> entry: stateGraph.entrySet()) {
				result.stateGraph.put(entry.getKey(), entry.getValue().copy());
			}
		}
		
		result.setShape(getShape());

		return result;
	}

	@Override
	public Set<String> getViewReferences() {
		Set<String> result = new HashSet<>();
		
		for(String attribute: getAttributeList()) {
			String viewName = getViewReference(attribute);
			if(viewName != null)
				result.add(viewName);
		}
		
		return result;
	}

	private String getViewReference(String attribute) {
		if(attribute.matches(VIEW_REFERENCE_NAME_REGEX)) {
			Scanner s = new Scanner(attribute);
			try {
				s.findInLine(VIEW_REFERENCE_NAME_REGEX);
				MatchResult result = s.match();
				if(result.groupCount() == 1)
					return result.group(1);
			} finally {
				s.close();
			}
		}

		return null;
	}

	@Override
	public boolean hasViewReference() {
		 
		for(String attribute: getAttributeList()) {
			if(getViewReference(attribute) != null)
				return true;
		}
		
		return false;
	}

	@Override
	public void expand() {
		// If it is already expanded then return
		if(isExpanded()) {
			return;
		}
		
		// First expand the children
		if(children != null) {
			for(View child: children) {
				child.expand();
			}
		}
		
		// Expand the native query if any
		if(nativeQuery != null) {
			nativeQuery.expand(this);
		}
		
		// Find and substitute the view references
		attributeList = getExpandedList(getAttributeList());

		// Get the RegEx attributes
		Map<String, Pattern> regexMap = new HashMap<>();
		Set<String> exactSet = new HashSet<>();
		for(String attrPath: this.attributeList) {
			if(DFAtoRE.isRegex(attrPath)) {
				regexMap.put(attrPath, Pattern.compile(attrPath));
			} else {
				exactSet.add(attrPath);
			}
		}
		if(regexMap.size() > 0) {
			this.setRegexAttributes(regexMap);
		}
		if(exactSet.size() > 0) {
			this.setExactAttributes(exactSet);
		}

		setExpanded(true);
	}

	@Override
	public List<String> getExpandedList(List<String> input) {
		// Find and substitute the view references
		List<String> newList = new ArrayList<>();
		for(String attribute: input) {
			if(getViewReference(attribute) != null) {
				newList.addAll(expand(attribute));
			} else {
				newList.add(attribute);
			}
		}
		
		return newList;
	}

	private List<String> expand(String attribute) {
		if(attribute.matches(VIEW_REFERENCE_MULTIPLE_REGEX)) {
			throw new IllegalStateException("Cannot refer to more than one view in an attribute: " + attribute);
		}
		String viewName = getViewReference(attribute);
		View view = getShape().getView(viewName);

		// Attributes might be referring to a RegEx expression
		if(view == null) {
			return this.attributeList;
		}

		if(!view.isExpanded()) {
			view.expand();
		}
		
		String prefix = attribute.substring(0, attribute.indexOf(VIEW_REFERENCE_START));
		List<String> expandedAttributes = new ArrayList<>();
		for(String suffix: view.getAttributeList()) {
			expandedAttributes.add(prefix + suffix);
		}
		
		return expandedAttributes;
	}

