	@Override
	public boolean matches(String path) {
		if(regexAttributes != null) {
			for(Pattern p: regexAttributes.values()) {
				Matcher matcher = p.matcher(path);
				if(matcher.matches() || matcher.hitEnd()) {
					return true;
				}
			}
		}

		return false;
	}
	
