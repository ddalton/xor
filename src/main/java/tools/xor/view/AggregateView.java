	@Override
	public StoredProcedure getStoredProcedure(final AggregateAction action) {
		StoredProcedure result = null;

		if(getStoredProcedure() != null) {
			for (StoredProcedure sp : getStoredProcedure()) {
				if (sp.getAction() == action) {
					result = sp.copy();
					break;
				}
			}
		}

		return result;
	}

