	private String getEntityName(EntityType type, StateGraph.Scope scope) {
		String namePrefix = scope.name() + ":";

		if(type.isDomainType()) {
			return namePrefix + DOMAIN + type.getName();
		} else {
			return namePrefix + type.getName();
		}
	}

