package tools.xor.db.base;

import javax.persistence.MappedSuperclass;

@MappedSuperclass
public class Identity extends Id {

	private String name;
	private String displayName;
	private String description;
	private String iconUrl;
	private String detailedDescription;
	private boolean isCriticalSystemObject;
	private String objectId;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDisplayName() {
		return displayName;
	}

	public void setDisplayName(String displayName) {
		this.displayName = displayName;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getIconUrl() {
		return iconUrl;
	}

	public void setIconUrl(String iconUrl) {
		this.iconUrl = iconUrl;
	}

	public String getDetailedDescription() {
		return detailedDescription;
	}

	public void setDetailedDescription(String detailedDescription) {
		this.detailedDescription = detailedDescription;
	}

	public void setIsCriticalSystemObject(Boolean value) {
		this.isCriticalSystemObject = value;
	}

	public Boolean getIsCriticalSystemObject() {
		return this.isCriticalSystemObject;
	}

	public String getObjectId() {
		return objectId;
	}

	public void setObjectId(String objectId) {
		this.objectId = objectId;
	}

}
